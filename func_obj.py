# пропишемте сначала функцию подбора элементов (предикат)
# например длинна строки
def is_longer_6(word):
    return len(word) > 6


words = ["гопник", "абстракция", "последовательность", "аргумент",
         "авторитет", "глазурь", "кофе", "печенька"]

for word in filter(is_longer_6, words):
    print(word)

# or this way:
results = filter(is_longer_6, words)
print(results)

print(list(filter(is_longer_6, words)))


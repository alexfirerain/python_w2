def starts_with_a(word):
    return word.startswith('а')


words = ["аквариум", "кино", "автоматические удовлетворители", "алиса", "зоопарк", "ном"]

print(set(filter(starts_with_a, words)))


# for работает и с генератором,
# но если хотим работать с ним как с последовательностью, то обернём его в список

def square(num):
    return num ** 2


nums = [1, 2, 5, 6, 11]

squares = map(square, nums)
print(list(squares))


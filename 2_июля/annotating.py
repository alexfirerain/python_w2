# return vs yield:
def generate_list():
    for i in range(5):
        return i


print(generate_list())


def generate_list():
    for i in range(5):
        yield i
# илд создаёт не значение, а Генератор (итерируемой последовательности)
# он, в отличие от Возврата, не кончает работу метода
# генератор содержит начальное значение и адрес генератора в памяти

print(generate_list())
print(list(generate_list()))    # генератор вливает результат в какую-нить коллекцию

